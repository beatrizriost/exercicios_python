# Entrada
largura = float(input(f'\n\tDigite as Dimensões do Paralelepípedo\n\tLargura: '))
altura = float(input(f'\n\tAltura: '))
comprimento = float(input(f'\n\tComprimento: '))

# Processamento
volume = largura * altura * comprimento

# Saída
print(f'\n\tO volume do paralelepípedo é : {volume}')
